// Function to mint a new NFT with an IPFS hash
export async function deleteDocument(tokenId) {
  try {
    const tx = await contract.deleteDocument(tokenId); // Send the transaction
    const receipt = await tx.wait(); // Wait for the transaction to be mined
    if (receipt && receipt.hash) {
      console.log("Document minted with IPFS hash:", receipt.hash, receipt.logs,receipt.logs[0].topics[3]);
      const tokenId =receipt.logs[0].topics[3]
      const decimalValue = parseInt(tokenId, 16);
     
      console.log(receipt.hash)
      return  decimalValue; // Return the transaction hash
    } else {
      console.error("Transaction was not mined or returned an undefined hash.");
      return null;
    }
  } catch (error) {
    console.error("Error during minting:", error);
    return null;
  }
}

// Function to retrieve the token URI
export async function getTokenURI(tokenId) {
  try {
    const uri = await contract.tokenURI(tokenId); // Retrieve the token URI
    console.log("Token URI:", uri);
    return uri;
  } catch (error) {
    console.error("Error retrieving token URI:", error);
    return null;
  }
}

